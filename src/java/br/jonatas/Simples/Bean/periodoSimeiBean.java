package br.jonatas.Simples.Bean;

/**
 *
 * @author issqn
 */
public class periodoSimeiBean {
    
    private int id;
    private String cnpj, dataInicio, dataFim, identificadorCancelamento, numeroOpcaoSimples, numeroOpcaoSimei;

    public periodoSimeiBean() {
    }
    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCnpj() {
        return cnpj;
    }

    public void setCnpj(String cnpj) {
        this.cnpj = cnpj;
    }

    public String getDataInicio() {
        return dataInicio;
    }

    public void setDataInicio(String dataInicio) {
        this.dataInicio = dataInicio;
    }

    public String getDataFim() {
        return dataFim;
    }

    public void setDataFim(String dataFim) {
        this.dataFim = dataFim;
    }

    public String getIdentificadorCancelamento() {
        return identificadorCancelamento;
    }

    public void setIdentificadorCancelamento(String identificadorCancelamento) {
        this.identificadorCancelamento = identificadorCancelamento;
    }

    public String getNumeroOpcaoSimples() {
        return numeroOpcaoSimples;
    }

    public void setNumeroOpcaoSimples(String numeroOpcaoSimples) {
        this.numeroOpcaoSimples = numeroOpcaoSimples;
    }

    public String getNumeroOpcaoSimei() {
        return numeroOpcaoSimei;
    }

    public void setNumeroOpcaoSimei(String numeroOpcaoSimei) {
        this.numeroOpcaoSimei = numeroOpcaoSimei;
    }

    
    
    
    
}
